package com.elcom.eodapp.media.common;

public class eInfoVideo {
	private int id;
	private String title;
	private String def;
	private String type;
	private String id_rvc;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDef() {
		return def;
	}
	public void setDef(String def) {
		this.def = def;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getId_rvc() {
		return id_rvc;
	}
	public void setId_rvc(String id_rvc) {
		this.id_rvc = id_rvc;
	}
	
}
