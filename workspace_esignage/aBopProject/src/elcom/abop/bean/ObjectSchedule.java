package elcom.abop.bean;

public class ObjectSchedule {
	private int timer;
	private String idPeriodic;
	private String idDaily;
	private String idGroup;

	public int getTimer() {
		return timer;
	}

	public void setTimer(int timer) {
		this.timer = timer;
	}

	public String getIdPeriodic() {
		return idPeriodic;
	}

	public void setIdPeriodic(String idPeriodic) {
		this.idPeriodic = idPeriodic;
	}

	public String getIdDaily() {
		return idDaily;
	}

	public void setIdDaily(String idDaily) {
		this.idDaily = idDaily;
	}

	public String getIdGroup() {
		return idGroup;
	}

	public void setIdGroup(String idGroup) {
		this.idGroup = idGroup;
	}

}
